-- ============================================================
--  Krent — бэкафилл маркетингового согласия перед включением
--  opt-in-гейта в рассылках (send.ts пропускает контакты без
--  consent_marketing). Существующие контакты, не помеченные
--  «не беспокоить», считаем согласившимися; новые — false.
-- ============================================================

update public.contacts
set consent_marketing = true
where do_not_contact = false
  and consent_marketing = false;
